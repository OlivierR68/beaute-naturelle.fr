			"COOKIES"<br /><br /></h3>
		</p>
		<p>Le site a éventuellement recours aux techniques de "cookies".</p>
		<p>Un "cookie" est un fichier de petite taille (moins de 4 ko), stocké par le site sur le disque dur de
			l'utilisateur, contenant des informations relatives aux habitudes de navigation de l'utilisateur.</p>
		<p>Ces fichiers lui permettent de traiter des statistiques et des informations sur le trafic, de faciliter
			la
			navigation et d'améliorer le service pour le confort de l'utilisateur.</p>
